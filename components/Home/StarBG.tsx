import type { CSSProperties } from 'react'
import { type ReactNode, useEffect, useState } from 'react'
import styled from 'styled-components'
import { useWindowSize } from '../Hooks'

interface Props {
  show: boolean
}

const StarBG = ({ show }: Props) => {
  const { width, height } = useWindowSize()

  const [stars, setStars] = useState<ReactNode[]>([])

  useEffect(() => {
    if (!show) setStars([])
    if (!width || !height || !show) return

    const interval = setInterval(() => {
      const randomNum = Math.floor(Math.random() * 3)
      const top = Math.random() * height
      const left = Math.random() * width
      const rotate = Math.random() * 90 - 45 // Random from -45 to 45
      const key = new Date().getTime()

      setStars((prev) =>
        [
          ...prev,
          <Star
            key={key}
            index={randomNum}
            style={{ '--top': `${top}px`, '--left': `${left}px`, '--rotate': `${rotate}deg` } as CSSProperties}
          />,
        ].slice(-(width / 20))
      )
    }, 750)

    return () => clearInterval(interval)
  }, [width, height, show])

  return <Container show={show}>{stars}</Container>
}

export default StarBG

